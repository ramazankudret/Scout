def oui_to_vendor(mac: str | None) -> str:
    """Return vendor name for MAC address, or 'Unknown' if not found."""
    if not mac or not isinstance(mac, str):
        return "Unknown"
    # Normalize: remove separators, take first 6 chars, uppercase
    raw = mac.replace(":", "").replace("-", "").replace(".", "").upper()
    if len(raw) >= 6:
        oui = raw[:6]
        return _OUI.get(oui, "Unknown")
    return "Unknown"
